/*Q.1: Create two integer variables length and breadth and assign values then
check if they are square values or rectangle values.
ie: if both values are equal then it's square otherwise rectangle. */

void main() {
  int length = 32;
  int breadth = 45;
  if (length == breadth) {
    print("Both values are equal so its a square");
  } else {
    print("Both values are not equal so its a rectangle");
  }
}
